class employee:
    
    aug_tx = 1.05

    def __init__(self, prenom, nom, paye):
        self.prenom = prenom
        self.nom = nom
        self.paye = int(paye)
        self.email = str(prenom + "." + nom + "@lol.user").lower()

    def mem_hex_adress(self):
        return hex(id(self))


    def mem_adress(self):
        return id(self)


    def aug_paye(self):
        self.paye = int(self.paye * self.aug_tx)


    @classmethod
    def set_aug_tx(cls, tx):
        cls.aug_tx = tx


    @classmethod
    def from_string(cls, string):
        prenom, nom, paye = string.split("-")
        return cls(prenom, nom, paye)



