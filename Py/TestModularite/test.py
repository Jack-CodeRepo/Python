def addition():
    a = input("saisissez le nombre de la table:")
    b = input("saisissez le nombre maximum multiplicateur:")
    c = 0
    b = b+1
    while b != c:
        print(a, "+ ", c, "= ", a + c)
        c = c+1

def multiplication():
    a = input("saisissez le nombre nombre de la table:")
    b = input("saisissez le nombre maximum multiplicateur:")
    c = 0
    b = b+1
    while b != c:
        print(a, "x ", c, "= ", a * c)
        c = c+1
