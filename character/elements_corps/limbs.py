class limbs():
    def __init__(self, category: str, side: str):

        self.__exists = True
        self.__side = side
        self.__cat = category


    # getter
    @property
    def limb_exist(self):
        return self.__exists

    @property
    def limb_side(self):
        return self.__side

    @property
    def categ(self):
        return self.__cat


    # setter
    @limb_exist.setter
    def limb_exist(self, value):
        self.__exists = value



